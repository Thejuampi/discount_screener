// ── Helper ────────────────────────────────────────────────────────────────────

/** Translate a key for a given language. Supports `{name}` placeholders. */
export function translate(key: string, lang: Lang, vars?: Record<string, string | number>): string {
  let s = T[key]?.[lang] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}

// ── React context + hook ──────────────────────────────────────────────────────

interface LanguageCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const Context = createContext<LanguageCtx | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem(STORAGE_KEY);
    return (saved === "es" || saved === "en") ? saved : "es";
  });

  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem(STORAGE_KEY, l);
  };

  const t = (key: string, vars?: Record<string, string | number>) => translate(key, lang, vars);

  return <Context.Provider value={{ lang, setLang, t }}>{children}</Context.Provider>;
}

export function useT(): LanguageCtx {
  const ctx = useContext(Context);
  if (!ctx) throw new Error("useT must be used within LanguageProvider");
  return ctx;
}
