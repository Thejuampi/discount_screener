// Email composition — polished, inline-styled, email-client-safe HTML.
// Table-based layout, solid colors (no fl/grid/gradient), max-width 600px.

import type { SetupLabel } from "./api";

type Lang = "es" | "en";

// Subject prefix so the user can filter these into an Outlook/Gmail folder.
const SUBJECT_PREFIX = "[Vantage]";

export interface EmailChange {
  symbol: string;
  company: string | null;
  from: SetupLabel;
  to: SetupLabel;
  score: number;
  priceCents: number;
  dailyChangeBps: number | null;
}

export interface EmailOpportunity {
  symbol: string;
  company: string | null;
  label: SetupLabel;
  score: number;
  priceCents: number;
  sector: string | null;
  upsidePct: number | null; // vs analyst target
  dailyChangeBps: number | null;
  earningsEpoch: number | null;
}

export interface EmailRiskAlert {
  symbol: string;
  text: string;
  severity: "warn" | "danger";
}

export interface DigestData {
  changes: EmailChange[];
  opportunities: EmailOpportunity[];
  risks: EmailRiskAlert[];
  lang: Lang;
  now: Date;
}

