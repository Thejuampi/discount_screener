// ── formatters ──
const money = (cents: number) =>
  `$${(cents / 100).toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
const labelName = (l: SetupLabel, lang: Lang) => LABELS[l]?.[lang] ?? l;
const labelColor = (l: SetupLabel) => (POS.includes(l) ? C.green : NEG.includes(l) ? C.red : C.gray);
const scoreStr = (s: number) => (s > 0 ? `+${s}` : `${s}`);
const dayCell = (bps: number | null) => {
  if (bps == null) return `<span style="color:${C.sub}">—</span>`;
  const v = bps / 100;
  const col = v > 0 ? C.green : v < 0 ? C.red : C.sub;
  const arrow = v > 0 ? "▲" : v < 0 ? "▼" : "";
  return `<span style="color:${col};font-weight:600">${arrow} ${v >= 0 ? "+" : ""}${v.toFixed(2)}%</span>`;
};

function badge(text: string, color: string): string {
  return `<span style="display:inline-block;padding:2px 8px;border-radius:10px;background:${color};color:#fff;font-size:12px;font-weight:700;white-space:nowrap">${text}</span>`;
}

function sectionHeader(text: string): string {
  return `<tr><td style="padding:24px 24px 10px">
    <table role="presentation" cellpadding="0" cellspacing="0"><tr>
      <td style="width:4px;background:${C.accent};border-radius:2px">&nbsp;</td>
      <td style="padding-left:10px;font-size:13px;font-weight:800;letter-spacing:.05em;text-transform:uppercase;color:${C.ink}">${text}</td>
    </tr></table></td></tr>`;
}

